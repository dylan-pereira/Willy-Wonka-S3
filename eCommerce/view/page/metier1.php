					<p>
						Véritable spécialiste du chocolat, le chocolatier-confiseur s’occupe de toute la préparation des chocolats et des confiseries.
						Retrouvez sur cette fiche toutes les informations pour savoir comment devenir chocolatier–confiseur (formation, salaire, qualités requises, témoignages). 
					</p>
				</div>
				<div>
					<h2>En quoi consiste le metier de chocolatier ?</h2>
					<p>
						<span class="nom">Nathan</span> : Ce métier consiste à utiliser des ingrédients très variés et confectionner des chocolats, truffes, bonbons, pralinés, mousses au chocolat, etc.
					</p>
					<p>
						<span class="nom">Eleanor</span> : Etre chocolatier c’est créer toutes sortes de confiseries. Pour cela, nous nous occupons de la préparation de la pâte et de la garniture puis de la cuisson et décoration pour ensuite les mettre en vente.
					</p>
					<p>
						<span class="nom">Eliza</span> : Ce métier a pour principale mission de concevoir des confiseries de A à Z, c’est à dire, de la préparation de la pâte aux garnitures, puis en fin de journée nous préparons les ganaches pour le lendemain.
					</p>
				</div>
				<div>
					<h2>Qu’est-ce que vous aimez le plus dans votre metier ?</h2>
					<p>
						<span class="nom">Nathan</span> : C’est un métier qui passionne, rare et très gratifiant car il est extrêmement bien perçu par la population.
					</p>
					<p>
						<span class="nom">Eleanor</span> : J’aime surtout le contact avec les clients, leur raconter des anecdotes sur les chocolats et les voir sourire en sortant de la boutique.
					</p>
					<p>
						<span class="nom">Eliza</span> : Ce que j’aime le plus c’est toute la partie créative de ce métier.
					</p>
				</div>
				<div>
					<h2>Qu’est-ce que vous aimez le moins dans votre metier ?</h2>
					<p>
						<span class="nom">Nathan</span> : C’est un métier très fatigant car nous passons de longues heures debout.
					</p>
					<p>
						<span class="nom">Eleanor</span> : Devoir travailler les weekends.
					</p>
					<p>
						<span class="nom">Eliza</span> : Les périodes de fêtes sont toujours les plus fatigantes pour nous car nous devons répondre à énormément de commandes ce qui nous laisse peu de temps pour nos vies personnelles.
					</p>
				</div>
				<div>
					<h2>Quelles sont les qualites requises pour exercer ce metier ?</h2>
					<p>
						<span class="nom">Nathan</span> : Il faut être passionné et créatif.
					</p>
					<p>
						<span class="nom">Eleanor</span> : Il est important d’aimer le contact client et bien évidemment d’être passionné et savoir transmettre cette passion.
					</p>
					<p>
						<span class="nom">Eliza</span> : Il faut être rigoureux, minutieux et être en quelque sorte un artiste car nous devons toujours innover.
					</p>
				</div>
				<div>
					<h2>Quelle est la formation pour devenir chocolatier ?</h2>
					<p>
						Il existe plusieurs diplômes pour exercer cette profession :
					</p>
					<ul>
						<li>CAP chocolatier – confiseur ;</li>
						<li>BTM chocolatier – confiseur ;</li>
						<li>MC pâtisserie, glacier, chocolatier, confiserie spécialisée…</li>
						<li>Bac pro boulanger pâtissier.</li>
					</ul>
				</div>
