					<h2>Quel est le salaire d’un chocolatier ?</h2>
					<p>
						Le salaire d’un(e) débutant(e) est le SMIC.
						Lorsque le chocolatier est à son compte, le salaire peut varier mais se situe, en moyenne, vers les 3000 € net mensuel.
					</p>
