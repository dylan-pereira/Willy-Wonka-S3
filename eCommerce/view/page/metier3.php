			<article>
				<div>
					<p>
						Vous aimez le contact humain et le secteur de la vente ? Découvrez le métier de commercial, également appelé attaché commercial, à travers les témoignages de Charline, Alex et Christine qui nous parlent de leur métier et missions. 
					</p>
				</div>
				<div>
					<h2>En quoi consiste le metier de commercial ?</h2>
					<p>
						<span class="nom">Charline</span> : J’exerce le métier de commercial depuis 5 ans. Mon métier consiste à gérer un portefeuille de clients, promouvoir les produits et services de l’entreprise pour laquelle je travaille et bien sûr rechercher des nouveaux clients.
					</p>
					<p>
						<span class="nom">Alex</span> : Je m’occupe de plusieurs clients en répondant à leurs besoins, organise des rendez-vous de partenariat pour élargir le périmètre de vente de l’entreprise, actualise les systèmes d’information commerciale et veille à la fidélisation des clients.
					</p>
					<p>
						<span class="nom">Christine</span> : Je travaille dans une entreprise internationale de télécommunication depuis quelques années. En tant qu’attachée commerciale, je prospecte les produits et services que l’on vend, expose tous les bénéfices que les clients y trouveront, les fidélise en leur proposant des services sur-mesure tout en effectuant des visites chez eux si nécessaire.
					</p>
				</div>
				<div>
					<h2>Qu’est-ce que vous aimez le plus dans votre metier ?</h2>
					<p>
						<span class="nom">Charline</span> : Le contact humain est sans aucun doute ce que je préfère dans ce métier.
					</p>
					<p>
						<span class="nom">Alex</span> : L’esprit d’équipe et pouvoir répondre positivement aux besoins des clients. Bien sûr les primes sur objectifs sont une source de motivation supplémentaire non négligeable.
					</p>
					<p>
						<span class="nom">Christine</span> : C’est une vraie opportunité de pouvoir intégrer une entreprise solide et en pleine croissance.
					</p>
				</div>
				<div>
					<h2>Qu’est-ce que vous aimez le moins dans votre metier ?</h2>
					<p>
						<span class="nom">Charline</span> : Gérer les réclamations.
					</p>
					<p>
						<span class="nom">Alex</span> : Les horaires changent souvent. Il est difficile d’organiser nos vies personnelles.
					</p>
					<p>
						<span class="nom">Christine</span> : Je n’aime pas devoir travailler avec des objectifs fixes à atteindre.
					</p>
				</div>
				<div>
					<h2>Quelles sont les qualites requises pour exercer ce metier ?</h2>
					<p>
						<span class="nom">Charline</span> : Pour exercer ce métier, il est nécessaire d’avoir le goût du contact et un dynamisme pour la vente.
					</p>
					<p>
						<span class="nom">Alex</span> : L’écoute et la persévérance sont des qualités indispensables pour être bon commercial.
					</p>
					<p>
						<span class="nom">Christine</span> : Il faut avoir une personnalité proactive et être organisé.
					</p>
				</div>
				<div>
					<h2>Quelle est la formation pour devenir commercial ?</h2>
					<p>
						Le niveau minimum pour devenir commercial(e) est le Bac + 2. De nombreuses formations existent dont la plupart sont les BTS :
					</p>
					<ul>
						<li>BTS commerce international ;</li>
						<li>BTS négociation et relation client ;</li>
						<li>BTS technico-commercial</li>
					</ul>
					<p>
						Il est possible de poursuivre les études et décrocher une licence pro (Bac+3) dans ce domaine ou encore un master (Bac + 5) en ingénierie d’affaire par exemple.
					</p>
				</div>
				<div>
					<h2>Y-a-t-il un concours de commercial ?</h2>
					<p>
						Non mais l’admission aux écoles de commerce se fait par la voie du concours.
					</p>
				</div>
				<div>
					<h2>Existe-t-il des ecoles specialisees dans ce domaine ?</h2>
					<p>
						Non
					</p>
				</div>
				<div>
					<h2>Quel est le salaire d’un commercial ?</h2>
					<p>
						Le salaire moyen d’un débutant est de 1400€ net mensuel, auquel il faut ajouter de nombreuses primes sur objectifs
					</p>
				</div>	
				<div>
					<h2>Quels conseils donneriez-vous aux personnes qui souhaitent exercer ce me 	tier ?</h2>
					<p>
						<span class="nom">Charline</span> : Je leur conseillerai d’apprendre à parler plusieurs langues car dans le milieu du commerce nous avons souvent besoin de communiquer avec des marchés et clients étrangers. L’anglais est à mon sens indispensable.
					</p>
					<p>
						<span class="nom">Alex</span>Chocolatier : Il est important d’avoir le goût pour les défis et être très rigoureux notamment lors des négociations.
					</p>
					<p>
						<span class="nom">Christine</span> : Soyez organisé, persévérant, enthousiaste et développez votre sens de la persuasion.
					</p>
				</div>
