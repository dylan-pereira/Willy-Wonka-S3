					<p>
						Le SMIC.
					</p>
				</div>	
				<div>
					<h2>Quels conseils donneriez-vous aux personnes qui souhaitent exercer ce me 	tier ?</h2>
					<p>
						<span class="nom">Manon</span> : C’est un métier intéressant et je pense qu’avec le boom de la vente en ligne, il est possible de trouver des emplois.
					</p>
					<p>
						<span class="nom">Eduardo</span> : Même si les entreprises acceptent les débutants, je vous conseille fortement de passer le CACES, qui vous autorisera la conduite d’engins.
					</p>
					<p>
						<span class="nom">Yaël</span> : Vous devez vraiment être capable de travailler dans le stress, dans des rythmes très rapides.
