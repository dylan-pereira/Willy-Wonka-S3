				<h2> Un preparateur a l'oeuvre</h2>
				<img src="images/preparateur.png" alt="photo d'un preparateur de commandes">
			</article>
			<article>
				<div>
					<p>
						Le préparateur de commandes prépare les colis des clients au sein d’un entrepôt logistique, suite à la réception d’un bon de commande.
					</p>
					<p>
						Manon, Eduardo et Yaël nous parlent aujourd’hui de ce métier issu du secteur logistique. 
					</p>
				</div>
				<div>
					<h2>En quoi consiste le metier de preparateur de commandes ?</h2>
					<p>
						<span class="nom">Manon</span> : Mon travail consiste à préparer les commandes des clients. Je travaille à partir du bon de commande, je récupère tous les produits demandés, puis je prépare les colis.
					</p>
					<p>
						<span class="nom">Eduardo</span>: Je travaille au sein d’un entrepôt logistique et je m’occupe de servir les commandes des clients. J’utilise généralement pour ça un matériel spécifique : logiciel de gestion, douchette à scanner, transpalette, chariot élévateur.
					</p>
					<p>
						<span class="nom">Yaël</span>: Je sillonne les rayons de notre magasin afin de préparer dans les meilleurs délais les commandes de nos clients : réception du bon de commande, récupération des produits, vérification et emballage des colis, affranchissement et expédition des commandes.
					</p>
				</div>
				<div>
					<h2>Qu’est-ce que vous aimez le plus dans votre metier ?</h2>
					<p>
						<span class="nom">Manon</span>: J’adore le rythme de mon métier, car je n’ai pas le temps de m’ennuyer.
					</p>
					<p>
						<span class="nom">Eduardo</span>: J’aime bien découvrir de nouveaux produits et j’apprécie aussi de traiter par téléphone avec les fournisseurs ou les clients, quad il le faut.
					</p>
					<p>
						<span class="nom">Yaël</span> : Je trouve le rythme du métier dynamique et j’apprécie également de pouvoir utiliser des engins spécifiques.
					</p>
				</div>
				<div>
					<h2>Qu’est-ce que vous aimez le moins dans votre metier ?</h2>
					<p>
						<span class="nom">Manon</span> : Les conditions ne sont pas si simples : nous sommes tout le temps debout, certains produits sont en environnement réfrigérés et la cadence est dense.
					</p>
					<p>
						<span class="nom">Eduardo</span> : Il y a énormément de travail et les délais sont serrés.
					</p>
					<p>
						<span class="nom">Yaël</span> : Parfois, nous faisons tout ce qu’il faut de notre côté, mais le transporteur perd notre colis ou se trompe. C’est toujours un peu rageant.
					</p>
				</div>
				<div>
					<h2>Quelles sont les qualites requises pour exercer ce metier ?</h2>
					<p>
						<span class="nom">Manon</span> : Il faut être dynamique, efficace.
					</p>
					<p>
						<span class="nom">Eduardo</span> : Soyez rigoureux, il faut aller vite et ne pas se tromper dans les colis.
					</p>
					<p>
						<span class="nom">Yaël</span> : Il faut savoir travailler vite et bien.
					</p>
				</div>
				<div>
					<h2>Quelle est la formation pour devenir preparateur de commandes ?</h2>
					<p>
						Les postes sont ouverts aux profils autodidactes, mais il existe néanmoins des formations spécifiques :
					</p>
					<ul>
						<li>CAP opérateur logistique ;</li>
						<li>Titre professionnel préparateur de commandes ;</li>
						<li>Baccalauréat professionnel logistique</li>
					</ul>
				</div>
				<div>
					<h2>Y a-t-il un concours de preparateur de commandes ?</h2>
					<p>
						Non
					</p>
				</div>
				<div>
					<h2>Existe-t-il des ecoles de preparateur de commandes ?</h2>
					<p>
						Non
					</p>
				</div>
